package com.oratakashi.design.foundation.color

/**
 * OraColorSchemeKeyTokens is an enum class that contains all the color key tokens used in the Orata Design System.
 * The values are derived from the [OrataDesignColorScheme] interface property names.
 * @author oratakashi
 * @since 1 Nov 2025
 */
enum class OraColorSchemeKeyTokens {
    Primary,
    OnPrimary,
    PrimaryContainer,
    OnPrimaryContainer,
    Secondary,
    OnSecondary,
    SecondaryContainer,
    OnSecondaryContainer,
    Tertiary,
    OnTertiary,
    TertiaryContainer,
    OnTertiaryContainer,
    Error,
    OnError,
    ErrorContainer,
    OnErrorContainer,
    Surface,
    SurfaceDim,
    OnSurface,
    SurfaceBright,
    SurfaceVariant,
    OnSurfaceVariant,
    SurfaceContainer,
    SurfaceContainerLowest,
    SurfaceContainerLow,
    SurfaceContainerHigh,
    SurfaceContainerHighest,
    InverseSurface,
    InverseOnSurface,
    InversePrimary,
    Outline,
    OutlineVariant,
    Scrim,
    Background,
    OnBackground,
    Warning,
    OnWarning,
    WarningContainer,
    OnWarningContainer,
    Success,
    OnSuccess,
    SuccessContainer,
    OnSuccessContainer,
    Info,
    OnInfo,
    InfoContainer,
    OnInfoContainer
}


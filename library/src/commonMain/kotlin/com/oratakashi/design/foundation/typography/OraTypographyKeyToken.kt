/**
 * OraTypographyKeyToken is an enum class that represents the different typography styles used in the Orata Design System.
 * @author oratakashi
 * @since 16 Sep 2025
 */
enum class OraTypographyKeyToken {
    DisplayLarge,
    DisplayMedium,
    DisplaySmall,
    HeadlineLarge,
    HeadlineMedium,
    HeadlineSmall,
    TitleLarge,
    TitleMedium,
    TitleSmall,
    LabelLarge,
    LabelMedium,
    LabelSmall,
    BodyLarge,
    BodyMedium,
    BodySmall
}
